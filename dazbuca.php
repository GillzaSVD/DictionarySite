        <p>Русская дактильная азбука (также дактилология) — вспомогательная система русского жестового языка, в которой каждому жесту одной руки (обычно неподвижной правой, согнутой в локте) соответствует буква русского языка; многие знаки внешне похожи на соответствующие буквы русского алфавита.
